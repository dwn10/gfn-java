package aufgaben;

public class Zahlenabfolge_5 {

    public static void main(String[] args) {
        // Initialisiere eine Variable für die aktuelle Zahl
        int n = 25;

        // Ausgabe der Zahlen
        for (int i = 0; i < 5; i++) {
            // Gebe die aktuelle Zahl aus
            System.out.println(n);

            // Erhöhe die aktuelle Zahl um 2
            n += 2;
        }
    }
}