package _010_090;

public class _060_ComparisonOperators {
    public static void main(String[] args) {

        // Vergleichsoperatoren
        // == != < > >= <=

        System.out.println(3 == 4); // false
        System.out.println(3 != 4); // true
        System.out.println(3 <= 4); // true
        System.out.println(3 < 3); // false

    }
}
